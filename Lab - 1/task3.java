public class task3 {
        public static void main(String[] args) {

            int intNum1 = 3;
            int intNum2 = 8;
    
            double doubleNum1 = 5.5;
            double doubleNum2 = 2.5;
    
            float floatNum1 = 7.3f;  
            float floatNum2 = 3.0f;
    
            // Perform arithmetic operations
            int sumInt = intNum1 + intNum2;
            int differenceInt = intNum1 - intNum2;
            int productInt = intNum1 * intNum2;
            int quotientInt = intNum1 / intNum2;
    
            double sumDouble = doubleNum1 + doubleNum2;
            double differenceDouble = doubleNum1 - doubleNum2;
            double productDouble = doubleNum1 * doubleNum2;
            double quotientDouble = doubleNum1 / doubleNum2;
    
            float sumFloat = floatNum1 + floatNum2;
            float differenceFloat = floatNum1 - floatNum2;
            float productFloat = floatNum1 * floatNum2;
            float quotientFloat = floatNum1 / floatNum2;
    
            // Print the results
            System.out.println("Integer Results:");
            System.out.println("Sum: " + sumInt);
            System.out.println("Difference: " + differenceInt);
            System.out.println("Product: " + productInt);
            System.out.println("Quotient: " + quotientInt);
    
            System.out.println("\nDouble Results:");
            System.out.println("Sum: " + sumDouble);
            System.out.println("Difference: " + differenceDouble);
            System.out.println("Product: " + productDouble);
            System.out.println("Quotient: " + quotientDouble);
    
            System.out.println("\nFloat Results:");
            System.out.println("Sum: " + sumFloat);
            System.out.println("Difference: " + differenceFloat);
            System.out.println("Product: " + productFloat);
            System.out.println("Quotient: " + quotientFloat);
    }
    
}
